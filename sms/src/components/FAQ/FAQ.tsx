const FAQ: React.FC = () => {
  const [openFaqItem, setOpenFaqItem] = useState<number | null>(null);
  const toggleFaq = (index: number) => {
    setOpenFaqItem(openFaqItem === index ? null : index);
  };

  return (
    <div className="faq__section">
      <h2 className="faq__section-title">Часто задаваемые вопросы</h2>
      <div className="faq__list">
        {faqList.map((item, idx) => (
          <div className="faq__item" key={idx}>
            <h3 className="faq__question" onClick={() => toggleFaq(idx)}>{item.question}</h3>
            {openFaqItem === idx && <p className="faq__answer">{item.answer}</p>}
          </div>
        ))}
      </div>
    </div>
  );
};

